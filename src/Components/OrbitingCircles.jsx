import React from "react";

export function OrbitingCircles({ children, iconSize = 40, radius = 150, reverse = false, speed = 4 }) {
  const icons = React.Children.toArray(children);
  
  return (
    <div className="relative" style={{ width: radius * 2.5, height: radius * 2.5 }}>
      {icons.map((icon, index) => {
        const angle = (360 / icons.length) * index;
        const delay = (speed / icons.length) * index;
        
        return (
          <div
            key={index}
            className="absolute"
            style={{
              width: iconSize,
              height: iconSize,
              left: "50%",
              top: "50%",
              marginLeft: -iconSize / 2,
              marginTop: -iconSize / 2,
              animation: `orbit ${speed}s linear infinite`,
              animationDelay: `-${delay}s`,
              animationDirection: reverse ? "reverse" : "normal",
              transformOrigin: `${iconSize / 2}px ${iconSize / 2}px`,
            }}
          >
            <div
              style={{
                transform: `rotate(${angle}deg) translateX(${radius}px) rotate(-${angle}deg)`,
              }}
            >
              {icon}
            </div>
          </div>
        );
      })}
      <style dangerouslySetInnerHTML={{
        __html: `
          @keyframes orbit {
            from { transform: rotate(0deg); }
            to { transform: rotate(360deg); }
          }
        `
      }} />
    </div>
  );
}

